                            INSERT INTO user (user_id, encrypted_password, role_id)
                            VALUES (CONCAT(year_var, role_var, LPAD(seq_var, 4, '0')),
                                    '$2a$10$4/dFbgH9MJ.RjFyPb8iFjOCbtlOYljZzrnyMGyau0hAkNdMZ2yoRO',
                                    role_var);

